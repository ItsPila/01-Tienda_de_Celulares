    public void mostrarDatos(){
        String[] titulos={"Codigo","Nombre","Apellido","Direcci√≥n","Celular","Cantidad"};
        String[] registros=new String[6];
        
        DefaultTableModel modelo = new DefaultTableModel(null,titulos);
        //TRANSACCION
        try{
            Statement st=conectando.createStatement();
            ResultSet resultado=st.executeQuery("Select c.id_cliente,"
                    + "c.nombre,c.apellido,c.direccion"
                    + ",c.celular,c.cantidad from"
                    + " cliente as c");
            
            while(resultado.next()){
                registros[0]=resultado.getString("id_cliente");
                registros[1]=resultado.getString("nombre");
                registros[2]=resultado.getString("apellido");
                registros[3]=resultado.getString("direccion");
                registros[4]=resultado.getString("celular");
                registros[5]=resultado.getString("cantidad");
                
                
                modelo.addRow(registros);
                
            }
            tbclientes.setModel(modelo);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }
    
    
